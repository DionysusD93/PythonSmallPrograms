def calculate_compound_interest(principal, rate, time):
    """
    Рассчитывает сложные проценты с учетом ежемесячной капитализации.

    Аргументы:
    principal -- основная сумма (начальный капитал)
    rate -- годовая процентная ставка (в десятичной форме, например, 0.01 для 1%)
    time -- период (в годах)

    Возвращает:
    Итоговую сумму после прошествия заданного периода времени.
    """

    # Рассчитываем ежемесячную процентную ставку
    monthly_rate = rate / 12

    # Рассчитываем общее количество периодов (в месяцах)
    total_periods = time * 12

    # Рассчитываем итоговую сумму после прошествия заданного периода времени
    total_amount = principal * (1 + monthly_rate) ** total_periods

    return total_amount

# Пример использования функции
principal = 100  # начальный капитал
rate = 0.10  # годовая процентная ставка (10%)
time = 1  # период в годах

result = calculate_compound_interest(principal, rate, time)
print("Итоговая сумма после {} лет: {:.2f}".format(time, result))
